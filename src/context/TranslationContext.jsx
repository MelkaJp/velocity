export const languages = [
  { code: 'en', name: 'English', native: 'English', dir: 'ltr' },
  { code: 'am', name: 'Amharic', native: 'አማርኛ', dir: 'rtl' },
  { code: 'or', name: 'Afaan Oromo', native: 'Afaan Oromoo', dir: 'ltr' },
  { code: 'tg', name: 'Tigrigna', native: 'ትግርኛ', dir: 'ltr' },
];

const LanguageContext = createContext();

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState('en');
  const [dir, setDir] = useState('ltr');

  useEffect(() => {
    const saved = localStorage.getItem('velocity_language');
    if (saved) {
      setLanguage(saved);
      const lang = languages.find(l => l.code === saved);
      if (lang) setDir(lang.dir);
    }
  }, []);

  const changeLanguage = (lang) => {
    const found = languages.find(l => l.code === lang);
    setLanguage(lang);
    setDir(found?.dir || 'ltr');
    localStorage.setItem('velocity_language', lang);
  };

  const t = (key, fallback) => {
    return translations[language]?.[key] || translations.en[key] || fallback || key;
  };

  return (
    <LanguageContext.Provider value={{ language, changeLanguage, t, dir, languages }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useTranslation() {
  const context = useContext(LanguageContext);
  if (!context) throw new Error('useTranslation must be used within LanguageProvider');
  return context;
}